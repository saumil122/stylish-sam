/* notification */
function showNotification(e, a) {
    var type = ["secondary", "light", "info", "danger", "success", "warning", "dark", "primary"];
    var color = Math.floor(6 * Math.random() + 1);
    $.notify({
        icon: "add_alert",
        message: "This is a simple notification generated by Bootstrap Notify js."
    }, {
        type: type[color],
        timer: 3e3,
        placement: {
            from: e,
            align: a
        }
    })
}

$(document).ready(function () {
    /* sidebar */
    $('.sidebar-menu a.nav-collapsed').click(function () {
        if ($(this).hasClass('open')) {
            $(this).next('ul.nav-second-level').slideUp();
            $(this).removeClass('open');
        } else {
            $(this).addClass('open');
            $(this).next('ul.nav-second-level').slideDown();
        }
    });

    /* accordion default behavior */
    $('.accordion-wrapper .accordion-heading').click(function () {
        if ($(this).hasClass('open')) {
            $(this).next('.accordion-content').slideUp();
            $(this).removeClass('open');
        } else {
            var opt = $(this).parents('.accordion-wrapper').attr('data-option');
            if (opt !== 'multiple') {
                var $openedDiv = $(this).parents('.accordion-wrapper').find('.accordion-heading.open');
                $openedDiv.next('.accordion-content').slideUp();
                $openedDiv.removeClass('open');
            }

            $(this).addClass('open');
            $(this).next('.accordion-content').slideDown();
        }
    });

    /* notify button click */
    $('.show-notify').click(function () {
        var e = $(this).attr('data-x');
        var a = $(this).attr('data-y');
        showNotification(e, a);
    });

});

